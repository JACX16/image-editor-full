    private void jPanel1MouseMoved(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel1MouseMoved
            //Movimento do mouse na imagem
        int posx, posy, vermelho, verde, azul, cinza;
        Imagem alt =new Imagem();
        posx=evt.getX();
        posy=evt.getY();
        vermelho=((Imagem)jPanel1).getVermelho(posx, posy);
        verde=((Imagem)jPanel1).getVerde(posx, posy);
        azul=((Imagem)jPanel1).getAzul(posx, posy);
        cinza=(vermelho+verde+azul)/3;
        
        jLabel1.setText("X " + String.valueOf(posx));
        jLabel2.setText("Y " + String.valueOf(posy));
        jLabel3.setText("Red " + String.valueOf(vermelho));
        jLabel4.setText("Green " + String.valueOf(verde));
        jLabel5.setText("Blue " + String.valueOf(azul));
        jLabel6.setText("Cinza " + String.valueOf(cinza));
        
    }//GEN-LAST:event_jPanel1MouseMoved

