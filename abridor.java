    private void jPanel1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel1MouseClicked

        double e, ci, ce;
        if((x1 == -1) && (y1 == -1))
        {
            x1 = evt.getX();
            y1 = evt.getY();
        }
        else
        {
            x2= evt.getX();
            y2 = evt.getY();
            e = ((Imagem)jPanel1).euclidiana(x1, x2, y1, y2);
            ci = ((Imagem)jPanel1).cityBlock(x1, x2, y1, y2);
            ce = ((Imagem)jPanel1).chessBoard(x1, x2, y1, y2);
            jLabel8.setText("Euclidiana "+String.valueOf(e));
            jLabel9.setText("City Block "+String.valueOf(ci));
            jLabel10.setText("Chess Board "+String.valueOf(ce));
            
        }
    
    }//GEN-LAST:event_jPanel1MouseClicked

    private void jPanel2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel2MouseClicked
        
        if(conectividade4 == 1)
        {
        Color cor = new Color(((Imagem)jPanel2).getFigura().getRGB(evt.getX(), evt.getY()));
        ((Imagem)jPanel2).vizinhanca4(evt.getX(), evt.getY(), cor);
        repaint();
        }
        if(conectividade8 == 1)
        {
        Color cor = new Color(((Imagem)jPanel2).getFigura().getRGB(evt.getX(), evt.getY()));
        ((Imagem)jPanel2).vizinhanca8(evt.getX(), evt.getY(), cor);
        repaint();
        }
    }//GEN-LAST:event_jPanel2MouseClicked

